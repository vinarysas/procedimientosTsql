			END

			SET @_fila = @_fila + 1
			FETCH NEXT FROM  ix INTO @ix__ID_CICLOFACTURACIONPDV, @ix__SECUENCIA, @ix__FECHAINICIO, @ix__FECHAFIN
		END;
