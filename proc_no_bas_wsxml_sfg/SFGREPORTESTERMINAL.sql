CREATE       PROCEDURE WSXML_SFG.SFGREPORTESTERMINAL_GetPagingParameters(@p_NUMEROPAGINA NUMERIC(22,0), @p_LIMITEMENOR NUMERIC(22,0) OUT, @p_LIMITEMAYOR NUMERIC(22,0) OUT) AS
   BEGIN
