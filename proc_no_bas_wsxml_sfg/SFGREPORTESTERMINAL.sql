 BEGIN
	  SET NOCOUNT ON;
    DECLARE @cCODCICLOFACTURACIONPDV NUMERIC(22,0);
    DECLARE @cFECHAULTIMAEJECUCION   DATETIME;

    DECLARE @xWITHDRW NUMERIC(22,0) = 4; -- Linea de negocio Retiros
    DECLARE @xBILLPAY NUMERIC(22,0) = 3; -- Linea de negocio Bill Payment
