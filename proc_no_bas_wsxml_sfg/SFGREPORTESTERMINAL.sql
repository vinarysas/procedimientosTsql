                          FROM WSXML_SFG.REGISTROFACTURACION
                          GROUP BY CODDETALLEFACTURACIONPDV) REG ON (REG.CODDETALLEFACTURACIONPDV = DFP.ID_DETALLEFACTURACIONPDV)
