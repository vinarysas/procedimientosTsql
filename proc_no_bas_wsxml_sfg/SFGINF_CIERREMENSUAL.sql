			EXEC WSXML_SFG.SFGCICLOFACTURACIONPDV_GetCicloDateRangeFromID @ix__ID_CICLOFACTURACIONPDV, @iniDATE OUT, @endDATE OUT
			SET @ix__FECHAINICIO = @iniDATE;
			SET @ix__FECHAFIN    = @endDATE;

