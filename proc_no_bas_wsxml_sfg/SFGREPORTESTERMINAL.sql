		SET @msg = 'No se pudo obtener la informaci?n base para los reportes de de cadenas: ' + isnull(ERROR_MESSAGE(), '');
		EXEC WSXML_SFG.SFGALERTA_GenerarAlerta @l_TIPOADVERTENCIA, 'GENERACIONXMLUNIFICADA', @msg, 1
	END CATCH
  END;
