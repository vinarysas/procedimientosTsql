                          ELSE CONVERT(VARCHAR, 'ADMINISTRACION PDV') END AS NOMTIPOCONTRATOPRODUCTO,
                     /* Valores */
                     /* Valores */
                     SUM(CASE WHEN REG.ANTICIPO = 0 THEN DFP.VALORCOMISION ELSE 0 END)     AS INGRESO,
